#include "vector.h"

t_vect	vect_cross(t_vect u, t_vect v)
{
	t_vect	w;

	w.x = u.y * v.z - v.y * u.z;
	w.y = u.z * v.x - v.z * u.x;
	w.z = u.x * v.y - v.x * u.y;
	return (w);
}
